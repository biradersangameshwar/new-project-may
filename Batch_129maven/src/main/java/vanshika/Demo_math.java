package vanshika;

public class Demo_math {

	public static void main(String[] args) {

		System.out.println(Math.abs(-10));
		System.out.println(Math.max(5,19));
		System.out.println(Math.min(30,400));
		System.out.println(Math.sqrt(45));
		System.out.println(Math.pow(2,4));
		System.out.println(Math.round(4));
		
	}

}
